/* IF STATEMENT NOTES

What is an if statement?
 - used to tell if a condition is true
 - important for making choices
 - can also use else for an alternate, or else if for an alternate with condition

Conditional comparisons
 - <, >, <=, >=, ==, != (!)
 - Used to compare two variables, often numbers

Conditional links
 - && - used to see if both conditions are true
 - || - used to see if at least one condition is true
 
*/